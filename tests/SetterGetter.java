class SetterGetter {
	int x;
	boolean amICool;
	char c;
	String name;

	int getX() {
		return this.x;
	}

	void setX(int x) {
		this.x = x;
	}

	boolean isAmICool() {
		return this.amICool;
	}

	boolean getAmICool() {
		return this.amICool;
	}

	void setAmICool(boolean amICool) {
		this.amICool = amICool;
	}

	char getC() {
		return this.c;
	}

	void setC(char c) {
		this.c = c;
	}

	String getName() {
		return this.name;
	}

	void setName(String name) {
		this.name = name;
	}
}
