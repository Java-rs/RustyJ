class SetterGetter {
	int x;
	boolean b;
	char c;
	String s;

	int getX() {
		return this.x;
	}

	void setX(int x) {
		this.x = x;
	}

	boolean getB() {
		return this.b;
	}

	void setB(boolean b) {
		this.b = b;
	}

	char getC() {
		return this.c;
	}

	void setC(char c) {
		this.c = c;
	}

	String getS() {
		return this.s;
	}

	void setS(String s) {
		this.s = s;
	}
}
